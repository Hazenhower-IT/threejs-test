<script setup>
  import { ref, onMounted, onUnmounted } from "vue"
  import { OrbitControls } from "three/examples/jsm/controls/OrbitControls"
  import { GLTFLoader } from "three/examples/jsm/loaders/GLTFLoader"
  import * as THREE from "three"

  
  let scene = new THREE.Scene()
  let renderer
  let controls

  let canvasRef = ref()

  let earth
  let loadedModel
  let ufo
  let eagle
  let spaceStation
  
  let satellite

  
  
  let camera = new THREE.PerspectiveCamera(
    75, //vertical field of view
    window.innerWidth / window.innerHeight, //aspect ratio
    0.1, //near plane
    1000 //far plane
  )
  camera.position.z = 30


  let pointLight = new THREE.PointLight(0xffffff)
  pointLight.position.set(5,5,5)
  
  let ambientLight = new THREE.AmbientLight(0xffffff)
  scene.add(pointLight, ambientLight)

  /*  UNCOMMENT FOR HELPER
  let lightHelper = new THREE.PointLightHelper(pointLight)
  scene.add(lightHelper)
  
  let gridHelper = new THREE.GridHelper(200,50)
  scene.add(gridHelper)
  */

  let loop = () => {

    earth.rotation.y += 0.005
    controls.update();
    renderer.render(scene, camera);

    if(satellite){
      satellite.rotation.z += 0.01
      satellite.rotation.x += 0.01
    }

    if(ufo){
      ufo.rotation.z += 0.01
    }
    
  };

  let addStar = () => {
    const geometry = new THREE.SphereGeometry(0.25, 24, 24)
    const material = new THREE.MeshStandardMaterial({color: 0xffffff})
    const star = new THREE.Mesh(geometry, material)

    const [x,y,z] = Array(3).fill().map(()=> THREE.MathUtils.randFloatSpread(100))
    star.position.set(x,y,z)
    scene.add(star)
  }

  Array(200).fill().forEach(addStar)

  
  let moveCamera = () =>{
    const t = document.body.getBoundingClientRect().top
    earth.rotation.x += 0.05
    earth.rotation.y += 0.075
    earth.rotation.z += 0.05

    if(satellite){
    satellite.rotation.x += 0.05
    satellite.rotation.y += 0.075
    satellite.rotation.z += 0.05
    }

    camera.position.z = t * -0.01
    camera.position.x = t * -0.0002
    camera.position.y = t * 0.0002


  }
  document.body.onscroll = moveCamera
  
  let resizeCallback = () => {
    renderer.setSize(window.innerWidth, window.innerHeight)
    camera.aspect = window.innerWidth / window.innerHeight
    camera.updateProjectionMatrix()
  }
  
  onMounted(() => {
    renderer = new THREE.WebGLRenderer({
      canvas: canvasRef.value,
      antialias: true,
      alpha: true,
    })
  
    renderer.setSize(window.innerWidth, window.innerHeight)
    renderer.setPixelRatio(window.devicePixelRatio)
    renderer.render(scene, camera)
  
    renderer.setAnimationLoop(loop)

    const spaceTexture = new THREE.TextureLoader().load("/dark-space2.webp")
    scene.background = spaceTexture


    const earthTexture = new THREE.TextureLoader().load("/earth.jpg")
    const normaltexture = new THREE.TextureLoader().load("/normal.jpg")
    earth = new THREE.Mesh(
      new THREE.SphereGeometry(3,32,32),
      new THREE.MeshStandardMaterial( {map: earthTexture, normalMap: normaltexture})
    )
    scene.add(earth)

    earth.position.z = 20;
    earth.position.x = -8;

    controls = new OrbitControls(camera, canvasRef.value)
    controls.enableDamping = true

    const gltfLoader = new GLTFLoader()
    gltfLoader.load('./models/satellite/scene.gltf', (model) =>{
      loadedModel = model.scene.children[0]
      satellite = loadedModel.clone()
      satellite.name = "satellite"
      satellite.position.z = 7;
      satellite.position.x = 1;

      scene.add(satellite)
    })

    gltfLoader.load('./models/ufo2/scene.gltf', (model) =>{
      loadedModel = model.scene.children[0]
      ufo = loadedModel.clone()
      ufo.name = "ufo"
      ufo.position.z = 45;
      ufo.position.x = 3;
      ufo.position.y = -2;
      ufo.rotation.x += 0.3
      ufo.scale.set(0.01,0.01,0.01)
      scene.add(ufo)
    })

    gltfLoader.load('./models/eagle-transporter/scene.gltf', (model) =>{
      loadedModel = model.scene.children[0]
      eagle = loadedModel.clone()
      eagle.name = "eagle-transporter"
      eagle.position.z = 55;
      eagle.position.x = 3;
      eagle.position.y = -3;
      eagle.rotation.x += 0.08
      eagle.rotation.z += Math.PI
      eagle.scale.set(0.1,0.1,0.1)
      scene.add(eagle)
    })

    gltfLoader.load('./models/ISS/scene.gltf', (model) =>{
      loadedModel = model.scene.children[0]
      spaceStation = loadedModel.clone()
      spaceStation.name = "ISS"
      spaceStation.position.z = 75;
      spaceStation.position.x = -5;
      spaceStation.position.y = -3;
      spaceStation.scale.set(0.3,0.5,0.3)
      scene.add(spaceStation)
    })

  
  
    window.addEventListener("resize", resizeCallback)
  })
  
  onUnmounted(() => {
    renderer.setAnimationLoop(null)
  })
  </script>
  
